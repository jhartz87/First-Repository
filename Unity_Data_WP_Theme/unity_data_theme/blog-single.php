          <br>
          <br> The threat of a data breach is real and on the rise. <a href="#">There has been a troubling trend of ransomware attacks on healthcare facilities.</a>
          <br>
          <br> Hackers used a variety of ways to gain access to hospital databases, but the most audacious method used was simply to log into the publically available wi-fi that many hospitals provide. Scary stuff right?
          <br>
          <br> With the free flow of patient-generated information becoming the norm, how does the healthcare industry guard against devastating attacks in the future? 4 steps need to be taken to keep the risk of a breach to a minimum.</p>
